char const* string_desc_arr [] = { (const char[]) { 0x09, 0x04 }, "Mothic Technologies", "Minimaid RP2040 clone" };
uint16_t const * tud_descriptor_string_cb(uint8_t index, uint16_t langid) {
    static uint16_t _desc_str[32];
    uint8_t chr_count;
    if (index == 0) { memcpy(&_desc_str[1], string_desc_arr[0], 2); chr_count = 1; }
    else {
        const char* str = string_desc_arr[index];
        chr_count = strlen(str);
        for(uint8_t i=0; i<chr_count; i++) _desc_str[1+i] = (uint16_t)str[i];
    }
    _desc_str[0] = (uint16_t)((TUSB_DESC_STRING << 8) | (2*chr_count + 2));
    return _desc_str;
}